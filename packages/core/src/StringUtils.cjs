    {
        let rxAddr = pOptions?.rxAddr || /\d+\s*((N|E|S|W|NE|NW|SE|SW)[. ]?)?\s*\w+\s*(AVE(NUE)?|BLV(D)?|ST(REET|[ .])|ROAD|RD([ .])|CIR(CLE)?|COURT|CT([ .])|DR(IVE|[ .])|LANE|LN([ .])|PL(ACE|[ .])|PARKWAY|PKWY|SQ(UARE|[ .])|TER(RACE|[ .])|ROUTE|RT([ .])|AVENUE|BOULEVARD)/gi;

        return (isRegExp( rxAddr ) && (rxAddr.test( pFirstName ) || rxAddr.test( pLastName )));
    }

    function normalizeName( pFirstName, pLastName, pOptions = PROPERCASE_OPTIONS, pOtherContactData = {} )
    {
        let options = { ...PROPERCASE_OPTIONS, ...(pOptions || {}) };

        let exceptions = _toArr( options.exceptions || PROPERCASE_OPTIONS.exceptions || [] ).filter( e => !isBlank( e ) ).map( e => _lct( e ) );

        let firstName = asProperCaseName( asString( pFirstName, true ).replaceAll( / {2,}/g, _spc ), options, true );
        let lastName = asProperCaseName( asString( pLastName, true ).replaceAll( / {2,}/g, _spc ), options, false );

        function _removeContactData( pName, pData = pOtherContactData )
        {
            let name = asString( pName, true );

            let data = pData || {};

            name = name.replace( (asString( data.address ) || data.address_line_1 || _mt), _mt );
            name = name.replace( (asString( data.phoneNumber || data.phone || data.phone_number ) || _mt), _mt );
            name = name.replace( (asString( data.emailAddress || data.email || data.email_address ) || _mt), _mt );

            return asString( name, true );
        }

        if ( isPopulatedObject( pOtherContactData ) )
        {
            firstName = asProperCaseName( _removeContactData( firstName, pOtherContactData ), options, true );
            lastName = asProperCaseName( _removeContactData( lastName, pOtherContactData ), options, false );
        }

        if ( firstName === lastName )
        {
            const parts = firstName.split( / / );

            if ( 2 === $ln( parts ) )
            {
                firstName = asString( parts[0], true );
                lastName = asString( parts[1], true );
            }
            else if ( $ln( parts ) > 2 )
            {
                let idx = _findSplitOnException( parts, exceptions );

                if ( idx < 0 )
                {
                    idx = _findSplitOnInitial( parts );
                }

                if ( idx < 0 )
                {
                    idx = _findSplitOnCompoundFirstName( parts, options );
                }

                if ( idx >= 0 )
                {
                    firstName = asProperCaseName( parts.slice( 0, idx ).join( _spc ), true );
                    lastName = asProperCaseName( parts.slice( idx ).join( _spc ), false );
                }
                else
                {
                    firstName = asProperCaseName( parts[0], true );
                    lastName = asProperCaseName( parts.slice( 1 ).join( _spc ), false );
                }
            }
        }
        else if ( $ln( lastName ) > $ln( firstName ) && lastName.startsWith( firstName ) && lastName.includes( _spc ) )
        {
            lastName = lastName.replace( firstName, _mt ).trim();
        }
        else if ( $ln( lastName ) < $ln( firstName ) && firstName.endsWith( lastName ) && firstName.includes( _spc ) )
        {
            firstName = firstName.replace( lastName, _mt ).trim();
        }

        return {
            first: asString( firstName, true ).replaceAll( / {2,}/g, _spc ),
            last: asString( lastName, true ).replaceAll( / {2,}/g, _spc ),
            nameContainsEmail: _nameContainsEmail( firstName, lastName, options ),
            nameContainsAddress: _nameContainsAddress( firstName, lastName, options )
        };
    }

    function normalizeEmailAddress( pEmail, pRemoveDots = false )
    {
        let email = lcase( asString( pEmail, true ) );

        if ( isBlank( email ) || $ln( email ) < 5 )
        {
            return email;
        }

        // handle some common typos
        email = asString( email, true ).replace( /\.(comm|con|cpm|cok|coj|co,)$/i, ".com" );
        email = asString( email, true ).replace( /(gmsil|gmial|gnail|gnial|gamil|gmaill)\.co\w+$/i, "gmail.com" );
        email = asString( email, true ).replace( /h[oi]t[mn][as]il\.co\w+$/i, "hotmail.com" );
        email = asString( email, true ).replace( /yaho+\.com/i, "yahoo.com" );
        email = asString( email, true ).replace( /@aol\.co\w+$/i, "@aol.com" );

        // remove "plus-addressing" from known providers that support it
        const tld = rightOfLast( email, "@" );

        if ( EMAIL_PROVIDERS_SUPPORTING_PLUS_VARIANTS.includes( tld ) )
        {
            email = asString( email, true ).replace( /\+[^@]+@/, "@" );

            if ( pRemoveDots && (["gmail.com", "googlemail.com"].includes( tld )) )
            {
                let parts = email.split( "@" );
                email = asString( parts[0], true ).replaceAll( /\./g, _mt ) + "@" + asString( parts[1] || tld, true );
            }
        }

        if ( email.includes( "@" ) )
        {
            let parts = _toArr( asString( email ).split( /@/ ) || [] );
            if ( $ln( parts ) > 1 )
            {
                let localPart = (asString( parts[0] || email, true ).slice( 0, 64 )).trim();
                let domainPart = (asString( parts[1] || tld, true ).slice( 0, (254 - $ln( localPart ) - 1) )).trim();
                email = (localPart.replaceAll( /[,\/;<]/g, "." ) + "@" + (domainPart || tld)).trim();
            }
        }

        return _lct( email );
    }

    const isValidEmail = function( pEmail )
    {
        let email = asString( pEmail, true );

        if ( isBlank( email ) || !(email.includes( "@" )) || $ln( email ) < 5 )
        {
            return false;
        }

        let parts = ((asString( email, true )).split( /@/ ) || []);

        let localPart = asString( parts[0] || email, true );

        let tld = (asString( (parts[1] || rightOfLast( email, "@" )), true ));

        if ( EMAIL_PROVIDERS_SUPPORTING_PLUS_VARIANTS.includes( tld ) )
        {
            localPart = localPart.replace( /\+\w+$/, _mt );
        }

        if ( $ln( localPart ) > 64 || ($ln( localPart + "@" + tld ) > 255) )
        {
            return false;
        }

        return !(/[:\[,\]`;&<(\\>)]/i).test( localPart );
    };

