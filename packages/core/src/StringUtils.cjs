    const reverseString = function( pStr )
    {
        let str = _resolveInput.call( this, pStr );

        if ( null == str || _ud === typeof str || isBlank( str ) )
        {
            return _mt_str;
        }

        let arr = [].concat( str );

        switch ( typeof str )
        {
            case _ud:
                arr = [];
                break;

            case _str:
                arr = asString( str ).split( _mt_chr );
                break;

            case _num:
            case _big:
                let s = asString( str );
                arr = s.split( _mt_chr );
                break;

            case _fun:
                arr = [];
                break;

            case _obj:

                if ( isArray( str ) )
                {
                    arr = [].concat( ...(str || []) );
                }

                break;

            default:
                break;
        }

        arr = arr.reverse();

        return asString( arr.join( _mt_chr ) );
    };

